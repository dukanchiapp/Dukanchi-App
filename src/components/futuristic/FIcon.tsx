export type FIconName = keyof typeof ICON_PATHS;

interface FIconProps {
  name: FIconName;
  size?: number;
  color?: string;
  /** When set to anything other than 'none', renders a filled glyph. */
  fill?: string;
  /** Stroke width for outline mode. */
  stroke?: number;
}

export function FIcon({
  name,
  size = 20,
  color = 'currentColor',
  fill = 'none',
  stroke = 2,
}: FIconProps): ReactElement {
  const filled = fill !== 'none';
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill={filled ? color : 'none'}
      stroke={filled ? 'none' : color}
      strokeWidth={filled ? 0 : stroke}
      strokeLinecap="round"
      strokeLinejoin="round"
      style={{ flexShrink: 0 }}
    >
      {ICON_PATHS[name]}
    </svg>
  );
}
